package com.kaushik.helpiez.client.model;

public class NgoClientModel {
	private String ngoName;
	private String ngoCause;
	private String ngoImage;
	private Integer ngoId;
	
	
	
	public Integer getNgoId() {
		return ngoId;
	}
	public void setNgoId(Integer ngoId) {
		this.ngoId = ngoId;
	}
	public String getNgoName() {
		return ngoName;
	}
	public void setNgoName(String ngoName) {
		this.ngoName = ngoName;
	}
	public String getNgoCause() {
		return ngoCause;
	}
	public void setNgoCause(String ngoCause) {
		this.ngoCause = ngoCause;
	}
	public String getNgoImage() {
		return ngoImage;
	}
	public void setNgoImage(String ngoImage) {
		this.ngoImage = ngoImage;
	}
	
	
	
}
